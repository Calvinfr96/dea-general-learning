-- Initial Setup
USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;

-- Create Database and Schemas
CREATE OR REPLACE DATABASE EMPLOYEE_DATA;
USE DATABASE EMPLOYEE_DATA;
CREATE OR REPLACE SCHEMA RAW_DATA;
CREATE OR REPLACE SCHEMA TRANSFORMED_DATA;

-- Create Tables
CREATE OR REPLACE TABLE EMPLOYEE_DATA.RAW_DATA.EMPLOYEE_RAW (
    EMPLOYEE_ID STRING,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    DEPARTMENT STRING,
    SALARY DECIMAL(10,2),
    HIRE_DATE DATE,
    LOCATION STRING
);

CREATE OR REPLACE TABLE EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED (
    EMPLOYEE_ID STRING,
    FULL_NAME STRING,
    DEPARTMENT STRING,
    ANNUAL_SALARY DECIMAL(10, 2),
    HIRE_DATE DATE,
    EXPERIENCE_LEVEL STRING,
    TENURE_DAYS STRING,
    STATE STRING,
    COUNTRY STRING,
    BONUS_ELIGIBILITY STRING,
    HIGH_POTENTIAL_FLAG STRING
);

-- Create a CSV File Format
USE SCHEMA EMPLOYEE_DATA.RAW_DATA; -- Need to specify schema before creating file format.
CREATE OR REPLACE FILE FORMAT CSV_FORMAT
    TYPE = 'CSV'
    FIELD_DELIMITER = ','
    RECORD_DELIMITER = '\n'
    SKIP_HEADER = 1;

-- Create a Stage
CREATE OR REPLACE STAGE EMPLOYEE_STAGE; -- Creating a named stage to upload external data.

-- Copy External Data Into EMPLOYEE_RAW Table
COPY INTO EMPLOYEE_DATA.RAW_DATA.EMPLOYEE_RAW
FROM @EMPLOYEE_STAGE
FILE_FORMAT = (FORMAT_NAME = CSV_FORMAT);

-- Perform Data Transformations and Insert Data Into EMPLOYEE_TRANSFORMED Table
INSERT INTO EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
    SELECT
        EMPLOYEE_ID,
        CONCAT(FIRST_NAME, ' ', LAST_NAME) AS FULL_NAME,
        DEPARTMENT,
        SALARY * 12 AS ANNUAL_SALARY,
        HIRE_DATE,
        (
            CASE
                WHEN DATEDIFF(YEAR, HIRE_DATE, CURRENT_DATE) < 1 THEN 'New Hire'
                WHEN DATEDIFF(YEAR, HIRE_DATE, CURRENT_DATE) >= 1 AND DATEDIFF(YEAR, HIRE_DATE, CURRENT_DATE) <= 5 THEN 'Mid-Level'
                ELSE 'Senior'
            END
        ) AS EXPERIENCE_LEVEL,
        DATEDIFF(DAY, HIRE_DATE, CURRENT_DATE) AS TENURE_DAYS,
        SUBSTRING(LOCATION, 1, CHARINDEX('-', LOCATION) - 1) AS STATE,
        SUBSTRING(LOCATION, CHARINDEX('-', LOCATION) + 1, LENGTH(LOCATION)) AS COUNTRY,
        (
            CASE
                WHEN SALARY > 10000 THEN 'Eligible'
                ELSE 'Not Eligible'
            END
        ) AS BONUS_ELIGIBILITY,
        (
            CASE
                WHEN DATEDIFF(YEAR, HIRE_DATE, CURRENT_DATE) > 3 THEN 'High Potential'
                ELSE 'Low Potential'
            END
        ) AS HIGH_POTENTIAL_FLAG
    FROM EMPLOYEE_DATA.RAW_DATA.EMPLOYEE_RAW;

-- Employee Count by Department
SELECT
    DEPARTMENT,
    COUNT(EMPLOYEE_ID) AS DEPARTMENT_EMPLOYEE_COUNT
FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
GROUP BY DEPARTMENT;

-- Employee Count by Country
SELECT
    COUNTRY,
    COUNT(EMPLOYEE_ID) AS COUNTRY_EMPLOYEE_COUNT
FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
GROUP BY COUNTRY;

-- Employees Hired With 12 Months
SELECT
    EMPLOYEE_ID,
    FULL_NAME,
    HIRE_DATE
FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
WHERE DATEDIFF(MONTH, HIRE_DATE, CURRENT_DATE) <= 12;

-- Top 10% of Employees by Salary
SELECT
    EMPLOYEE_ID,
    DEPARTMENT,
    FULL_NAME,
    ANNUAL_SALARY
FROM (
    SELECT
        EMPLOYEE_ID,
        DEPARTMENT,
        FULL_NAME,
        ANNUAL_SALARY,
        NTILE(10) OVER (ORDER BY ANNUAL_SALARY DESC) AS BUCKET
    FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
)
WHERE BUCKET = 1;

-- Total Salary Expense by Department per Year
SELECT
    DEPARTMENT,
    YEAR(HIRE_DATE) AS YEAR,
    SUM(ANNUAL_SALARY) AS TOTAL_SALARY_EXPENSE
FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
GROUP BY DEPARTMENT, YEAR(HIRE_DATE);

-- Employees With Tenure of 5+ Years
SELECT
    EMPLOYEE_ID,
    FULL_NAME,
    HIRE_DATE
FROM EMPLOYEE_DATA.TRANSFORMED_DATA.EMPLOYEE_TRANSFORMED
WHERE DATEDIFF(YEAR, HIRE_DATE, CURRENT_DATE) >= 5;