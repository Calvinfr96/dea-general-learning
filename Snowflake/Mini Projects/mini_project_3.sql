-- Initial Setup
USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;

-- Create Database and Schemas
CREATE OR REPLACE DATABASE SALES_DATA;
USE DATABASE SALES_DATA;
CREATE OR REPLACE SCHEMA RAW_DATA;
CREATE OR REPLACE SCHEMA FLATTENED_DATA;

-- Create Tables
CREATE OR REPLACE TABLE SALES_DATA.RAW_DATA.SALES_RAW (
    DATA VARIANT
);

CREATE OR REPLACE TABLE SALES_DATA.FLATTENED_DATA.SALES_FLATTENED (
    COMPANIES STRING,
    SALES_PERIOD STRING,
    TOTAL_REVENUE FLOAT,
    TOTAL_UNITS_SOLD FLOAT,
    REGIONS STRING,
    TOTAL_SALES FLOAT,
    PRODUCTS STRING,
    UNITS_SOLD FLOAT,
    REVENUE FLOAT
);

-- Create JSON File Format
USE SCHEMA SALES_DATA.RAW_DATA;
CREATE OR REPLACE FILE FORMAT JSON_FORMAT
    TYPE = 'JSON';

-- Create Stage
CREATE OR REPLACE STAGE SALES_STAGE;

-- Load Data From SALES_STAGE to SALES_RAW
COPY INTO SALES_DATA.RAW_DATA.SALES_RAW
FROM @SALES_STAGE
FILE_FORMAT = (FORMAT_NAME = JSON_FORMAT);

-- Perform Data Transformation and Insert Into SALES_FLATTENED Table
INSERT INTO SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
    SELECT
        companies.key::STRING AS COMPANIES,
        companies.value:sales_period::STRING AS SALES_PERIOD,
        companies.value:total_revenue::FLOAT AS TOTAL_REVENUE,
        companies.value:total_units_sold::FLOAT AS TOTAL_UNITS_SOLD,
        regions.key::STRING AS REGIONS,
        regions.value:total_sales::FLOAT AS TOTAL_SALES,
        products.key::STRING AS PRODUCTS,
        products.value:units_sold::FLOAT AS UNITS_SOLD,
        products.value:revenue::FLOAT AS REVENUE
    FROM SALES_DATA.RAW_DATA.SALES_RAW,
    LATERAL FLATTEN (INPUT => DATA:companies) AS companies,
    LATERAL FLATTEN (INPUT => companies.value:regions) AS regions,
    LATERAL FLATTEN (INPUT => regions.value:products) AS products;

SELECT * FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED LIMIT 5;

-- Total Revenue Per Company
SELECT
    COMPANIES,
    TOTAL_REVENUE
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
GROUP BY COMPANIES, TOTAL_REVENUE;

-- Top 3 Companies With The Highest Revenue
SELECT
    COMPANIES,
    TOTAL_REVENUE
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
GROUP BY COMPANIES, TOTAL_REVENUE
ORDER BY TOTAL_REVENUE DESC
LIMIT 3;

-- Bottom 2 Regions By Total Units Sold
SELECT
    REGIONS,
    SUM(TOTAL_UNITS_SOLD) AS TOTAL_REGIONAL_UNITS_SOLD
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
GROUP BY REGIONS
ORDER BY TOTAL_REGIONAL_UNITS_SOLD
LIMIT 2;

-- Product With Highest Sales Revenue
SELECT
    PRODUCTS,
    SUM(REVENUE) AS TOTAL_PRODUCT_REVENUE
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
GROUP BY PRODUCTS
ORDER BY TOTAL_PRODUCT_REVENUE DESC
LIMIT 1;

-- Product With Fewest Units Sold
SELECT
    PRODUCTS,
    SUM(UNITS_SOLD) AS TOTAL_PRODUCT_UNITS_SOLD
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
GROUP BY PRODUCTS
ORDER BY TOTAL_PRODUCT_UNITS_SOLD
LIMIT 1;

-- Region With Highest Number of Laptop Units Sold
SELECT
    REGIONS,
    SUM(TOTAL_UNITS_SOLD) AS TOTAL_LAPTOPS_SOLD
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
WHERE PRODUCTS = 'laptop'
GROUP BY REGIONS
ORDER BY TOTAL_LAPTOPS_SOLD DESC
LIMIT 1;

-- Bottom 3 Companies and Regions By Smartphone Sales Revenue
SELECT
    COMPANIES,
    SUM(REVENUE) AS TOTAL_SMARTPHONE_REVENUE
FROM SALES_DATA.FLATTENED_DATA.SALES_FLATTENED
WHERE PRODUCTS = 'smartphone'
GROUP BY COMPANIES
ORDER BY TOTAL_SMARTPHONE_REVENUE
LIMIT 3;