-- Initial Setup
USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;

-- Create Database and Schemas
CREATE OR REPLACE DATABASE CUSTOMER_DATA;
USE DATABASE CUSTOMER_DATA;
CREATE OR REPLACE SCHEMA RAW_DATA;
CREATE OR REPLACE SCHEMA FLATTENED_DATA;

-- Create Tables
CREATE OR REPLACE TABLE CUSTOMER_DATA.RAW_DATA.CUSTOMER_RAW (
    DATA VARIANT
);

CREATE OR REPLACE TABLE CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED (
    CUSTOMERID INT,
    NAME STRING,
    EMAIL STRING,
    REGION STRING,
    COUNTRY STRING,
    PRODUCTNAME STRING,
    PRODUCTBRAND STRING,
    CATEGORY STRING,
    QUANTITY INT,
    PRICEPERUNIT FLOAT,
    TOTALSALES FLOAT,
    PURCHASEMODE STRING,
    MODEOFPAYMENT STRING,
    PURCHASEDATE DATE
);

-- Create JSON File Format
USE SCHEMA CUSTOMER_DATA.RAW_DATA;
CREATE OR REPLACE FILE FORMAT JSON_FORMAT
    TYPE = 'JSON';

-- Create Stage
CREATE OR REPLACE STAGE CUSTOMER_STAGE;

-- Load Data From CUSTOMER_STAGE Into CUSTOMER_RAW Table
COPY INTO CUSTOMER_DATA.RAW_DATA.CUSTOMER_RAW
FROM @CUSTOMER_STAGE -- No need to use PARSE_JSON when loading into table because the file format is already JSON.
FILE_FORMAT = (FORMAT_NAME = JSON_FORMAT);

-- Perform Data Transformation and Insert Into CUSTOMER_FLATTENED Table
INSERT INTO CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
    SELECT
        customers.value:customerid::INT AS CUSTOMERID,
        customers.value:name::STRING AS NAME,
        customers.value:email::STRING AS EMAIL,
        customers.value:region::STRING AS REGION,
        customers.value:country::STRING AS COUNTRY,
        customers.value:productname::STRING AS PRODUCTNAME,
        customers.value:productbrand::STRING AS PRODUCTBRAND,
        customers.value:category::STRING AS CATEGORY,
        customers.value:quantity::INT AS QUANTITY,
        customers.value:priceperunit::FLOAT AS PRICEPERUNIT,
        customers.value:totalsales::FLOAT AS TOTALSALES,
        customers.value:purchasemode::STRING AS PURCHASEMODE,
        customers.value:modeofpayment::STRING AS MODEOFPAYMENT,
        customers.value:purchasedate::DATE AS PURCHASEDATE
    FROM CUSTOMER_DATA.RAW_DATA.CUSTOMER_RAW,
    LATERAL FLATTEN (INPUT => DATA) AS customers;

-- Total Sales Per Region
SELECT
    REGION,
    SUM(TOTALSALES) AS TOTAL_REGIONAL_SALES
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY REGION;

-- Region With Highest Total Sales
SELECT
    REGION,
    SUM(TOTALSALES) AS TOTAL_REGIONAL_SALES
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY REGION
ORDER BY TOTAL_REGIONAL_SALES DESC
LIMIT 1;

-- Total Quantity Sold Per Brand
SELECT
    PRODUCTBRAND,
    SUM(QUANTITY) AS TOTAL_QUANTITY_SOLD
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY PRODUCTBRAND;

-- Product With Least Quantity Sold
SELECT
    PRODUCTBRAND,
    SUM(QUANTITY) AS TOTAL_QUANTITY_SOLD
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY PRODUCTBRAND
ORDER BY TOTAL_QUANTITY_SOLD
LIMIT 1;

-- Customer With Highest Purchase
SELECT
    CUSTOMERID,
    NAME,
    EMAIL,
    TOTALSALES
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
ORDER BY TOTALSALES DESC
LIMIT 1;

-- Product Name and Brand With Lowest Unit Price
SELECT
    PRODUCTNAME,
    PRODUCTBRAND
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
ORDER BY PRICEPERUNIT DESC
LIMIT 1;

-- Top 5 Best-Selling Products
SELECT
    PRODUCTNAME,
    SUM(QUANTITY) AS TOTAL_QUANTITY_SOLD
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY PRODUCTNAME
ORDER BY TOTAL_QUANTITY_SOLD DESC
LIMIT 5;

-- Bottom 5 Countries by Total Sales
SELECT
    COUNTRY,
    SUM(TOTALSALES) AS TOTAL_NATIONAL_SALES
FROM CUSTOMER_DATA.FLATTENED_DATA.CUSTOMER_FLATTENED
GROUP BY COUNTRY
ORDER BY TOTAL_NATIONAL_SALES
LIMIT 5;
