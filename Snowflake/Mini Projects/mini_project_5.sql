-- Initial Setup
USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;

-- Create Database, Schema, and Table
CREATE OR REPLACE DATABASE PRODUCT_DB;
USE DATABASE PRODUCT_DB;
CREATE OR REPLACE SCHEMA PRODUCT_DATA;

CREATE OR REPLACE TABLE PRODUCT_DB.PRODUCT_DATA.PRODUCT_SRC (
    PRODUCTID STRING,
    PRODUCTNAME STRING,
    CATEGORY STRING,
    PRICE FLOAT,
    STOCKQUANTITY FLOAT,
    SUPPLIER STRING,
    RATING FLOAT
);

CREATE OR REPLACE TABLE PRODUCT_DB.PRODUCT_DATA.PRODUCT_TGT (
    PRODUCTID STRING,
    PRODUCTNAME STRING,
    CATEGORY STRING,
    PRICE FLOAT,
    STOCKQUANTITY FLOAT,
    SUPPLIER STRING,
    RATING FLOAT
);

-- Create Stream on PRODUCT_SRC Table (Tracks Changes in Source Table)
CREATE OR REPLACE STREAM PRODUCT_DB.PRODUCT_DATA.PRODUCT_STREAM
ON TABLE PRODUCT_DB.PRODUCT_DATA.PRODUCT_SRC; -- Standard Stream.

-- Create Task (Periodically Merges Source Table Changes With Target Table)
CREATE OR REPLACE TASK PRODUCT_DB.PRODUCT_DATA.PRODUCT_TASK
WAREHOUSE = COMPUTE_WH
SCHEDULE = '1 MINUTE'
AS
MERGE INTO PRODUCT_DB.PRODUCT_DATA.PRODUCT_TGT AS TGT
USING PRODUCT_DB.PRODUCT_DATA.PRODUCT_STREAM AS SRC
ON SRC.PRODUCTID = TGT.PRODUCTID
WHEN MATCHED THEN
UPDATE SET
    TGT.PRODUCTID = SRC.PRODUCTID, -- The target table must be specified first when updating during a merge.
    TGT.PRODUCTNAME = SRC.PRODUCTNAME,
    TGT.CATEGORY = SRC.CATEGORY,
    TGT.PRICE = SRC.PRICE,
    TGT.STOCKQUANTITY = SRC.STOCKQUANTITY,
    TGT.SUPPLIER = SRC.SUPPLIER,
    TGT.RATING = SRC.RATING
WHEN NOT MATCHED THEN
INSERT (
    PRODUCTID,
    PRODUCTNAME,
    CATEGORY,
    PRICE,
    STOCKQUANTITY,
    SUPPLIER,
    RATING
)
VALUES (
    SRC.PRODUCTID,
    SRC.PRODUCTNAME,
    SRC.CATEGORY,
    SRC.PRICE,
    SRC.STOCKQUANTITY,
    SRC.SUPPLIER,
    SRC.RATING
);

ALTER TASK PRODUCT_DB.PRODUCT_DATA.PRODUCT_TASK RESUME; -- Execute Task. Use SUSPEND to pause task.

-- Create File Format
CREATE OR REPLACE FILE FORMAT PRODUCT_DB.PRODUCT_DATA.CSV_FORMAT
    TYPE = 'CSV'
    FIELD_DELIMITER = ','
    RECORD_DELIMITER = '\n'
    SKIP_HEADER = 1;

-- Create Stage
CREATE OR REPLACE STAGE PRODUCT_DB.PRODUCT_DATA.PRODUCT_STAGE;

-- Copy Data From PRODUCT_DB.PRODUCT_DATA.PRODUCT_STAGE Into PRODUCT_DB.PRODUCT_DATA.PRODUCT_SRC
COPY INTO PRODUCT_DB.PRODUCT_DATA.PRODUCT_SRC
FROM @PRODUCT_DB.PRODUCT_DATA.PRODUCT_STAGE
FILE_FORMAT = (FORMAT_NAME = PRODUCT_DB.PRODUCT_DATA.CSV_FORMAT);

SELECT
    SRC.PRODUCTID,
    SRC.PRODUCTNAME,
    SRC.CATEGORY,
    SRC.PRICE,
    SRC.STOCKQUANTITY,
    SRC.SUPPLIER,
    SRC.RATING
FROM PRODUCT_DB.PRODUCT_DATA.PRODUCT_STREAM AS SRC LIMIT 5; -- When the task executes, data is removed from the stream after it is copied into the target table.
SHOW TASKS; -- Check if task is active.
SELECT * FROM TABLE (INFORMATION_SCHEMA.TASK_HISTORY(TASK_NAME => 'PRODUCT_TASK')); -- Show task execution history.
SELECT * FROM PRODUCT_DB.PRODUCT_DATA.PRODUCT_TGT WHERE PRODUCTID IN ('P001', 'P050', 'P075', 'P100') LIMIT 5; -- Check if data was updated by task.